library IEEE;
use IEEE.STD_LOGIC_1164.ALL;
use IEEE.NUMERIC_STD.ALL;


entity CLO is
    port (
        A : in std_logic_vector(31 downto 0);
        ones : out std_logic_vector(31 downto 0)
    );
end CLO;

architecture Behavioral of CLO is

begin
    ones <= x"00000000" when (A = "0-------------------------------") else
            x"00000001" when (A = "10------------------------------") else
            x"00000002" when (A = "110-----------------------------") else
            x"00000003" when (A = "1110----------------------------") else
            x"00000004" when (A = "11110---------------------------") else
            x"00000005" when (A = "111110--------------------------") else
            x"00000006" when (A = "1111110-------------------------") else
            x"00000007" when (A = "11111110------------------------") else
            x"00000008" when (A = "111111110-----------------------") else
            x"00000009" when (A = "1111111110----------------------") else
            x"0000000A" when (A = "11111111110---------------------") else
            x"0000000B" when (A = "111111111110--------------------") else
            x"0000000C" when (A = "1111111111110-------------------") else
            x"0000000D" when (A = "11111111111110------------------") else
            x"0000000E" when (A = "111111111111110-----------------") else
            x"0000000F" when (A = "1111111111111110----------------") else
            x"00000010" when (A = "11111111111111110---------------") else
            x"00000011" when (A = "111111111111111110--------------") else
            x"00000012" when (A = "1111111111111111110-------------") else
            x"00000013" when (A = "11111111111111111110------------") else
            x"00000014" when (A = "111111111111111111110-----------") else
            x"00000015" when (A = "1111111111111111111110----------") else
            x"00000016" when (A = "11111111111111111111110---------") else
            x"00000017" when (A = "111111111111111111111110--------") else
            x"00000018" when (A = "1111111111111111111111110-------") else
            x"00000019" when (A = "11111111111111111111111110------") else
            x"0000001A" when (A = "111111111111111111111111110-----") else
            x"0000001B" when (A = "1111111111111111111111111110----") else
            x"0000001C" when (A = "11111111111111111111111111110---") else
            x"0000001D" when (A = "111111111111111111111111111110--") else
            x"0000001E" when (A = "1111111111111111111111111111110-") else
            x"0000001F" when (A = "11111111111111111111111111111110") else
            x"00000020" when (A = "11111111111111111111111111111111") else
            (others => '0');

end Behavioral;
